    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try {
            empId = txtIdEmp.getText();
            empPass = txtPassEmp.getText();
            Class.forName("com.mysql.jdbc.Driver");
            Connection c = DriverManager.getConnection("jdbc:mysql://localhost:3306/fastway", "root", "root");
//            if(c!=null){JOptionPane.showMessageDialog(null, "connected");}
            String getData = "SELECT name, user_id, password FROM fastway.employee WHERE user_id=? AND password=?";
            PreparedStatement pst = c.prepareStatement(getData);
            pst.setString(1, empId);
            pst.setString(2, empPass);
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                empName = rs.getString(1);
                logLoading.setVisible(true);
                EmployeePage em = new EmployeePage(empName);
                em.setVisible(true);
                LoginPage.this.dispose();
            } else {
                logLoading.setVisible(false);
                JOptionPane.showMessageDialog(null, "Id or Password Is Incorrect");
            }

        } catch (ClassNotFoundException ex) {
            ex.printStackTrace();
        } catch (SQLException ex) {
            ex.printStackTrace();

        }
    }//GEN-LAST:event_jButton4ActionPerformed
