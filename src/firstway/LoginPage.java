    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // TODO add your handling code here:
        String emppin = "EMPLOYEE100";
        String uid = txtEmpId.getText();
        String nam = txtEmpName.getText();
        String pass = txtEmpPin.getText();
        String addr = txtEmpAddress.getText();
        String cont = txtEmpContact.getText();
        String getpin = txtEmpPin.getText();

        if (emppin.matches(getpin)) {
            try {
                Class.forName("com.mysql.jdbc.Driver");
                Connection c = DriverManager.getConnection("jdbc:mysql://localhost:3306/fastway", "root", "root");
//              if(c!=null){JOptionPane.showMessageDialog(null, "connected");}
                String adminData = "INSERT INTO fastway.employee (user_id, password, name, address, contact, rank) VALUES (?, ?, ?, ?, ?, ?)";
                PreparedStatement pst = c.prepareStatement(adminData);
                pst.setString(1, uid);
                pst.setString(2, pass);
                pst.setString(3, nam);
                pst.setString(4, addr);
                pst.setString(5, cont);
                pst.setString(6, "Employee");

                int i = pst.executeUpdate();
                if (i > 0) {
                    JOptionPane.showMessageDialog(null, "Registration Successful");
                    JOptionPane.showMessageDialog(null, "Login to Access");
                    LoginOption.setVisible(false);
                    adminLogin.setVisible(false);
                    employeeLogin.setVisible(true);
                    Registration.setVisible(false);
                    AdminRegistration.setVisible(false);
                    EmployeeRegistration.setVisible(false);
                }

            } catch (ClassNotFoundException ex) {
                ex.printStackTrace();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Pin is incorrect!");
        }
    }//GEN-LAST:event_jButton10ActionPerformed

